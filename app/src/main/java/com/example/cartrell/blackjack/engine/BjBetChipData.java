package com.example.cartrell.blackjack.engine;

class BjBetChipData {
  //=========================================================================
  // members
  //=========================================================================
  private int m_value;
  private String m_id;
  private String m_drawableId;

  //=========================================================================
  // package-private
  //=========================================================================

  //-------------------------------------------------------------------------
  // ctor
  //-------------------------------------------------------------------------
  BjBetChipData(String id, int value, String drawableId) {
    m_id = id;
    m_value = value;
    m_drawableId = drawableId;
  }

  //-------------------------------------------------------------------------
  // getDrawableId
  //-------------------------------------------------------------------------
  String getDrawableId() {
    return(m_drawableId);
  }

  //-------------------------------------------------------------------------
  // getId
  //-------------------------------------------------------------------------
  String getId() {
    return(m_id);
  }

  //-------------------------------------------------------------------------
  // getValue
  //-------------------------------------------------------------------------
  int getValue() {
    return(m_value);
  }
}
